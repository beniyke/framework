    /**
     * Custom disposable domains added via configuration
     */
    private static array $customDomains = [];

    /**
     * Get all disposable email domains
     */
    public static function get(): array
    {
        return array_merge(self::$domains, self::$customDomains);
    }

    public static function isDisposable(string $domain): bool
    {
        $domain = strtolower(trim($domain));
        $allDomains = self::get();

        return in_array($domain, $allDomains, true);
    }

    public static function addCustom(array $domains): void
    {
        $domains = array_map('strtolower', array_map('trim', $domains));
        self::$customDomains = array_unique(array_merge(self::$customDomains, $domains));
    }

    public static function removeCustom(array $domains): void
    {
        $domains = array_map('strtolower', array_map('trim', $domains));
        self::$customDomains = array_diff(self::$customDomains, $domains);
    }

    /**
     * Clear all custom domains
     */
    public static function clearCustom(): void
    {
        self::$customDomains = [];
    }

    public static function count(): int
    {
        return count(self::get());
    }
